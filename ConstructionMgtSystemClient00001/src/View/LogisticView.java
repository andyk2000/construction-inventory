    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        product_table.removeAll();
        String sh = Search.getText();
        int total=0;
        int q=0;
        int u=0;
        model1.setRowCount(0);
        try{

            Registry registry = LocateRegistry.getRegistry(5000);
            InventoryService Iserv= (InventoryService)registry.lookup("Inventory");
            List<Inventory> requestList = Iserv.getAllInventory();
            Iterator iterator = requestList.iterator();
            while(iterator.hasNext()){
                Inventory inv= (Inventory)iterator.next();
                if(sh.equals(inv.getProductName())){
                    q=Integer.parseInt(inv.getQuantity());
                    u=Integer.parseInt(inv.getUnitePrice());
                    total=q*u;
                    model1.addRow(new Object[]{
                        inv.getProductID(),
                        inv.getProductName(),
                        inv.getQuantity(),
                        inv.getUnitePrice(),
                        total
                    });
                    q=0;
                    u=0;
                    total=0;
                }
            }
            request_table.setModel(model1);

        }catch(Exception ex){
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButton8ActionPerformed

