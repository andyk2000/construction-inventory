    public void table_row_fill(){
        try{
            
            Registry registry = LocateRegistry.getRegistry(5000);
            RequestService Iserv= (RequestService)registry.lookup("Request");
            List<Request> requestList = Iserv.getAllRequest();
            Iterator iterator = requestList.iterator();
            while(iterator.hasNext()){
                Request inv= (Request)iterator.next();
                model2.addRow(new Object[]{
                    inv.getRequestID(),
                    inv.getEngineerEmail(),
                    inv.getProductName(),
                    inv.getQuantity(),
                    inv.getAnswer()
                });
            }
            request_table.setModel(model2);
            
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
    
    public void inv_row_fill(){
        int total=0;
        int q=0;
        int u=0;
        try{
            
            Registry registry = LocateRegistry.getRegistry(5000);
            InventoryService Iserv= (InventoryService)registry.lookup("Inventory");
            List<Inventory> requestList = Iserv.getAllInventory();
            Iterator iterator = requestList.iterator();
            while(iterator.hasNext()){
                Inventory inv= (Inventory)iterator.next();
                q=Integer.parseInt(inv.getQuantity());
                u=Integer.parseInt(inv.getUnitePrice());
                total=q*u;
                model1.addRow(new Object[]{
                    inv.getProductID(),
                    inv.getProductName(),
                    inv.getQuantity(),
                    inv.getUnitePrice(),
                    total
                });
                q=0;
                u=0;
                total=0;
            }
            product_table.setModel(model1);
            
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }

